DROP TABLE IF EXISTS PACKAGE;
DROP TABLE IF EXISTS PACKAGE_ITEM;
DROP TABLE IF EXISTS USER;
DROP TABLE IF EXISTS BONUS_FOR_PACKAGE;


CREATE TABLE PACKAGE
(
    ID      IDENTITY,
    NAME    VARCHAR,
    USER_ID INTEGER,
    STATUS  VARCHAR
)
;

CREATE TABLE PACKAGE_ITEM
(
    ID         IDENTITY,
    PACKAGE_ID INTEGER,
    NAME       VARCHAR,
    PRICE      INTEGER
)
;

CREATE TABLE USER
(
    ID   IDENTITY,
    NAME VARCHAR,
    ROLE VARCHAR
)
;

CREATE TABLE BONUS_FOR_PACKAGE
(
    ID IDENTITY,
    PACKAGE_ID INTEGER,
    BONUS_AMOUNT INTEGER
)
;
